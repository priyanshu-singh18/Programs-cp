class Solution
{
	public:
		string is_palindrome(int n)
		{
		    // Code here.
		    int num = n;
		    int temp = 0;
		    while(num > 0){
		        temp = temp*10+num%10;
		        num=num/10;
		    }
		    if(n == temp){
		        return "Yes";
		    }
		    return "No";
		}
};

//{ Driver Code Starts.
int main(){
    int T;
    cin >> T;
    while(T--)
    {
    	int n;
    	cin >> n;
    	Solution ob;
    	string ans = ob.is_palindrome(n);
    	cout << ans <<"\n";
    }
	return 0;
}

